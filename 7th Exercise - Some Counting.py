#7 Exercise: Some Counting
print("1) Count up 0 to 50:")
for i in range(0, 51):  
    print(i)
print()  


print("2) Count down 50 to 0:")
for i in range(50, -1, -1):  
    print(i)
print()


print("3) Count up 30 to 50:")
for i in range(30, 51): 
    print(i)
print()


print("4) Count down 50 to 10 by 2s:")
for i in range(50, 9, -2): 
    print(i)
print()

print("5) Count up 100 to 200 by 5s:")
for i in range(100, 201, 5):  
    print(i)
print()